    private void entrarLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarLoginActionPerformed
        String usuario=txt_nombreLogin.getText();
        String cd=txt_contraseLogin.getText();
        menu_principal objeto_ventana=new menu_principal();
        menu_principal_empleado pv= new menu_principal_empleado();
        
        if(cd.equals("tienda1234")&& usuario.equals("admin")){
            objeto_ventana.setVisible(true);
            this.dispose();
        }
        else if(cd.equals("empleado")&& usuario.equals("cajero01")){
            pv.setVisible(true);
            this.dispose();
        }
        else if(cd.equals("") || usuario.equals("")){
           JOptionPane.showMessageDialog(this,"las cajas estan vacias");
           LimpiarEntradas();
        }
        else{
            JOptionPane.showMessageDialog(this,"contrase√±a o nombre incorrecto");
            LimpiarEntradas();
        }
        
    }//GEN-LAST:event_entrarLoginActionPerformed

